    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            String platNomor = platNomorTextField.getText().replace(" ", "");
            int nominalBayar = (nominalTextField.getText().equals("Masukkan Nominal Bayar")) ? 0 : Integer.parseInt(nominalTextField.getText());
            Parkir parkir = parkirC.searchByPlateNum(platNomor);
            if (parkir != null) {
                int totalBayar = pembayaranC.hitungTotalBayar(parkir);
                
                if (nominalBayar >= totalBayar) {
                    if (parkir.getTipeParkir().equalsIgnoreCase("PERJAM")){
                        ((ParkirPerJam) parkir).keluarParkir();
                    } else {
                        ((ParkirInap) parkir).keluarParkir();
                    }
                    parkirC.updateParkir(parkir);
                    Pembayaran pembayaran = new Pembayaran(totalBayar, LocalDateTime.now(), parkir, Session.sesi.petugas);
                    pembayaranC.insertPembayaran(pembayaran);
                    pembayaranC.addPembayaran(pembayaran);
                    new Dashboard().setVisible(true);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(KeluarParkir.this, "Nominal Pembayaran tidak sesuai. Mohon refresh atau masukkan nominal yang sesuai");
                } 
            } else {
                JOptionPane.showMessageDialog(KeluarParkir.this, "Data kendaraan tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(KeluarParkir.this, "Mohon masukkan data kendaraan");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String platNomor = platNomorTextField.getText();
        Parkir parkir = parkirC.searchByPlateNum(platNomor);
        if (parkir != null && parkir.getStatusParkir().equals("PARKIR")){
            typeOfVehicleValueLabel.setText(parkir.getJenisKendaraan());
            if (parkir.getTipeParkir().equalsIgnoreCase("PERJAM")) {
                arrivalTimeValueLabel.setText(((ParkirPerJam) parkir).getWaktuMasuk().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss")));
                exitTimeValueLabell.setText(LocalDateTime.now().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss")));
                ((ParkirPerJam) parkir).setWaktuKeluar(LocalDateTime.now());
            } else {
                arrivalTimeValueLabel.setText(((ParkirInap) parkir).getTanggalMasuk().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
                exitTimeValueLabell.setText(LocalDate.now().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
                ((ParkirInap) parkir).setTanggalKeluar(LocalDate.now());
            }
        
            jLabel17.setText(String.valueOf(pembayaranC.hitungTotalBayar(parkir)));
        } else {
            JOptionPane.showMessageDialog(KeluarParkir.this, "Data Kendaraan Tidak Ditemukan");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void changeAmountCheckButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeAmountCheckButtonActionPerformed
        try {
            String platNomor = platNomorTextField.getText();
            int nominalBayar = Integer.parseInt(nominalTextField.getText());
            
            Parkir parkir = parkirC.searchByPlateNum(platNomor);
            nominalBayar = Integer.parseInt(nominalTextField.getText()); 
            int totalKembalian = nominalBayar - pembayaranC.hitungTotalBayar(parkir);
        
            if (totalKembalian < 0) {
                jLabel11.setText(String.valueOf("Nominal Tidak Valid"));
            } else {
                jLabel11.setText(String.valueOf(totalKembalian));
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(KeluarParkir.this, "Mohon masukkan data kendaraan");
        }
        
    }//GEN-LAST:event_changeAmountCheckButtonActionPerformed

